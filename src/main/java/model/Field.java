package model;

/**
 * Represents the academic field or department of a course.
 * Each course is associated with one of these fields.
 */
public enum Field {
    /** Mathematics */
    MAT,
    /** Computer Science */
    CSI,
    /** Software Engineering */
    SEG,
    /** Biology */
    BIO,
    /** Computer Engineering */
    CEG,
    /** English */
    ENG,
    /** Physics */
    PHY,
    /** Chemistry */
    CHE,
    /** Economics */
    ECO,
    /** History */
    HIS,
    /** Philosophy */
    PHI
}
